static const size_t nand_chips_count = sizeof(nand_chips) / sizeof(nand_chips[0]);

const nand_chip_t *nand_find_by_id(uint16_t chip_id, uint32_t ext_id) {
    for (size_t i = 0; i < nand_chips_count; i++) {
        if (nand_chips[i].chip_id == chip_id && nand_chips[i].ext_id == ext_id)
            return &nand_chips[i];
    }
    return NULL;
}

const nand_chip_t *nand_find_by_name(const char *name) {
    if (!name)
        return NULL;
    for (size_t i = 0; i < nand_chips_count; i++) {
        if (strcasecmp(nand_chips[i].name, name) == 0)
            return &nand_chips[i];
    }
    return NULL;
}

const nand_chip_t *nand_list(size_t *count) {
    if (count)
        *count = nand_chips_count;
    return nand_chips;
}
