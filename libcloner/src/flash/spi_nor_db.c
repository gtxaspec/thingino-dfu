static const size_t spi_nor_chips_count = sizeof(spi_nor_chips) / sizeof(spi_nor_chips[0]);

const spi_nor_chip_t *spi_nor_find_by_id(uint32_t jedec_id) {
    for (size_t i = 0; i < spi_nor_chips_count; i++) {
        if (spi_nor_chips[i].jedec_id == jedec_id)
            return &spi_nor_chips[i];
    }
    return NULL;
}

const spi_nor_chip_t *spi_nor_find_by_name(const char *name) {
    if (!name)
        return NULL;
    for (size_t i = 0; i < spi_nor_chips_count; i++) {
        if (strcasecmp(spi_nor_chips[i].name, name) == 0)
            return &spi_nor_chips[i];
    }
    return NULL;
}

const spi_nor_chip_t *spi_nor_list(size_t *count) {
    if (count)
        *count = spi_nor_chips_count;
    return spi_nor_chips;
}
